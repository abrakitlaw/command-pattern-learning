package com.company;

public class GarageDoor {

    public GarageDoor() {
    }

    public void up() {
        System.out.println("Garage is opening up");
    }

    public void down() {
        System.out.println("Garage is closing down");
    }

    public void stop() {
        System.out.println("Garage is stop");
    }

    public void lightOn() {
        System.out.println("Light is on");
    }

    public void lightOff() {
        System.out.println("Light is off");
    }
}
